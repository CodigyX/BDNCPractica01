    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Long clave = Long.parseLong(txtClave.getText());

        Producto producto = daoE.findById(clave);

        if (producto != null) {
            producto.setDescripcion(txtDescripcion.getText());
            producto.setPrecioVenta(Double.parseDouble(txtPrecioVenta.getText()));
            producto.setPrecioCompra(Double.parseDouble(txtPrecioCompra.getText()));

            daoE.modificar(producto);

            JOptionPane.showMessageDialog(null, "Exito.");
            vaciarTXT();
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró ");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBuscarAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAllActionPerformed
        
        String[] columnNames = {"Clave", "Descripcion", "Precio Venta", "Precio Compra"};
        List<Producto> productos = daoE.findAll();
        pro = new ProductoTableModel<Producto>(columnNames, productos) {
            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                switch (columnIndex) {
                    case 0:
                        return lstValores.get(rowIndex).getId();
                    case 1:
                        return lstValores.get(rowIndex).getDescripcion();
                    case 2:
                        return lstValores.get(rowIndex).getPrecioVenta();
                    case 3:
                        return lstValores.get(rowIndex).getPrecioCompra();
                    default:
                        return null;
                }
            }
        };

        jTableProductos.setModel(pro);
    }//GEN-LAST:event_btnBuscarAllActionPerformed


